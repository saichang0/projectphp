    <script>
        document.querySelectorAll("aside nav ul li a").forEach(link => {
            link.addEventListener("click", function(e) {
                // Prevent the page from reloading
                e.preventDefault();

                // Remove the active class from all links
                document.querySelectorAll("aside nav ul li a").forEach(l => {
                    l.classList.remove("text-orange-500", "font-semibold", "bg-gray-200");
                });

                // Add active class to the clicked link
                this.classList.add("text-orange-500", "font-semibold", "bg-gray-200");

                // Save the active link to localStorage
                localStorage.setItem("activeNavLink", this.getAttribute("href"));

                // Optionally, navigate to the link after style change
                window.location.href = this.getAttribute("href");
            });
        });

        window.addEventListener("DOMContentLoaded", () => {
            const activeHref = localStorage.getItem("activeNavLink");
            if (activeHref) {
                const activeLink = document.querySelector(`aside nav ul li a[href='${activeHref}']`);
                if (activeLink) {
                    activeLink.classList.add("text-orange-500", "font-semibold", "bg-gray-200");
                }
            }
        });
    </script>

